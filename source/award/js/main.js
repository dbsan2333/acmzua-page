function getAward(s) {
    if (s.endsWith('一等奖') || s.endsWith('金奖'))
        return 'gold';
    else if (s.endsWith('二等奖') || s.endsWith('银奖'))
        return 'silver';
    else if (s.endsWith('三等奖') || s.endsWith('铜奖'))
        return 'bronze';
    else return 'iron';
}
